// função é um liquidificador

function vitaminada(fruta1, fruta2){
  return 'vitaminada de: ' + fruta1 + fruta2
}
//precisamos armazenar o resultado em algum lugar
// no caso vamos pegar um copo pra por a vitaminada
const copo = vitaminada('mamão', 'laranja')

console.log(copo)
